export const formatDateAndTimeToDate = (dateAndTimeString, lang = "uz") => {
  const re = /([0-9]{4})-([0-9]{2})-([0-9]{2})/;
  const date = dateAndTimeString.match(re);
  return `${date[3]}-${months[lang][parseInt(date[2]) - 1]} ${date[1]}`;
};
